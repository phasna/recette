function fixAllBugs() {
  console.log("üîß √âTAPE 1: Correction de tous les bugs...");

  // 1.1 Intercepter tous les appels fetch
  console.log("1.1 Interception des appels fetch...");
  if (!window.originalFetch) {
    window.originalFetch = window.fetch;
  }

  window.fetch = function (url, options = {}) {
    if (url.includes("localhost:3000/api")) {
      const token = localStorage.getItem("token");
      if (token) {
        options.headers = {
          ...options.headers,
          Authorization: `Bearer ${token}`,
          "Content-Type": "application/json",
        };
        console.log("üîë Token ajout√© √†:", url);
      }
    }
    return window.originalFetch(url, options);
  };

  // 1.2 Corriger les intercepteurs axios
  console.log("1.2 Correction des intercepteurs axios...");
  if (window.axios) {
    window.axios.interceptors.request.clear();
    window.axios.interceptors.request.use((config) => {
      const token = localStorage.getItem("token");
      if (token) {
        config.headers.Authorization = `Bearer ${token}`;
      }
      return config;
    });
  }

  // 1.3 Synchroniser l'√©tat React
  console.log("1.3 Synchronisation de l'√©tat React...");
  const token = localStorage.getItem("token");
  const user = localStorage.getItem("user");

  if (token && user) {
    const authEvent = new CustomEvent("authStateChanged", {
      detail: { user: JSON.parse(user), token: token },
    });
    window.dispatchEvent(authEvent);
  }

  console.log("‚úÖ Tous les bugs corrig√©s");
}

